package com.vti.filter;

public class GroupFilter {
	private Short minMember;
	private Short maxMember;

	private Short minId;
	private Short maxId;

	public GroupFilter() {
	}

	public Short getMinMember() {
		return minMember;
	}

	public void setMinMember(Short minMember) {
		this.minMember = minMember;
	}

	public Short getMaxMember() {
		return maxMember;
	}

	public void setMaxMember(Short maxMember) {
		this.maxMember = maxMember;
	}

	public Short getMinId() {
		return minId;
	}

	public void setMinId(Short minId) {
		this.minId = minId;
	}

	public Short getMaxId() {
		return maxId;
	}

	public void setMaxId(Short maxId) {
		this.maxId = maxId;
	}


	
	
	

}
